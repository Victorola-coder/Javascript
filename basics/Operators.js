let x;

// 1. Arithmetic

x = 5 + 5; // addition - 10

x = 5 - 5; // subtraction - 0

x = 5 * 5; // multiplication - 25

x = 5 / 5; // division - 1

x = 10 % 3; // operand - handles remainder 1

// 2. Concatenation - joining of two or more data type mostly string, we use the addition operator to concatenate
x = "Hello" + " " + "World"; // Hello World

// 3. Exponential

x = 4 ** 4; // 4 raise to power 4 = 235

x = 3 ** 3; // 27

// 4. Assignment Operator

x = 7;

x += 7; // same as x = x + 7 -- repeat for all arithmetic operator cases

// 5. INCREMENT

x = 7;

x++; //  x + 1 = 8

// 6. DECREMENT

x--; // x -1 = 7 cos x is originally 8

console.log(x);

// JS Inbuilt MATH object, it allows you to perform mathematical task

let int;

int = Math.random() * 100; // generates random number between  0-99

int = Math.pow(4, 3); // 4^3 == 64

int = Math.floor(4.9); // 4- round down to the whole number

int = Math.ceil(4.1); // 5 - round up to the whole number

int = Math.round(4.5); // 5 - round to the nearest whole number

int = Math.abs(-7); // returns th absolute value

int = Math.min(1, 2, 3, 4, 5); // returns the minimum value - 1

int = Math.max(1, 2, 3, 4, 5); // returns the maximum value - 5

int = Math.sqrt(25); // returns the square root of the number - 5

int = Math.log(100); // returns the logarithm of the number - 2.302585092994046

int = Math.log10(100); // returns the logarithm of the number - 2.0

console.log(int);

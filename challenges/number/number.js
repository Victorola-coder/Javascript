let x = Math.floor(Math.random() * 100 + 1); // generates random number from 1 to 100

let y = Math.floor(Math.random() * 50 + 1); // generates random number between 1-50

// console.log(x, y);

const sum = x + y;
const summation = `${x} + ${y} = ${sum}`;

console.log(summation);

const difference = x - y;

const subtraction = `${x} - ${y} = ${difference}`;

console.log(subtraction);

const product = x * y;

const multiplication = `${x} * ${y} = ${product}`;

console.log(multiplication);

const quotient = x / y;

const division = `${x} / ${y} = ${quotient}`;

console.log(division);

const remainder = x % y;

const modulus = `${x} % ${y} = ${remainder}`;

console.log(modulus);
